package com.example.crimereporter;

public class UserInformation {

    public String name, city, country, postAddress, mobileNo, p1, p2;

    public UserInformation(){

    }

    public UserInformation(String name, String city, String country, String postAddress, String mobileNo, String p1, String p2) {
        this.name = name;
        this.city= city;
        this.country= country;
        this.postAddress= postAddress;
        this.mobileNo= mobileNo;
        this.p1=p1;
        this.p2=p2;

    }

}
